package com.example.hugo.bottomnavbar.Search;

public class Dog {
    public String name;
    public String breed;
    public int age;
    public String profileImageUrl;

    public Dog() {}

    public Dog(String name, String breed, int age, String profileImageUrl) {
        this.name = name;
        this.breed = breed;
        this.age = age;
        this.profileImageUrl = profileImageUrl;
    }
}